#!/bin/sh

echo " "
echo "---------------------------------------------------------------------------------------------------------------"
echo "----- install Extra Packages for Enterprise Linux (EPEL)"
echo "---------------------------------------------------------------------------------------------------------------"
echo " "

yum --enablerepo=extras install -y epel-release

echo " "
echo "---------------------------------------------------------------------------------------------------------------"
echo "----- install ansible"
echo "---------------------------------------------------------------------------------------------------------------"
echo " "

yum install -y ansible

echo " "
echo "---------------------------------------------------------------------------------------------------------------"
echo "----- run ansible"
echo "---------------------------------------------------------------------------------------------------------------"
echo " "

cd /vagrant/ansible
ansible-playbook main.yml --limit "mgmt,admin,repo,dn1"




